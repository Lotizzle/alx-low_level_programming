#include "main.h"

/**
 * add - sums two integers
 * a: first integer
 * b: second integer
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - subtracts two integers
 * a: first integer
 * b: second integer
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - multiplies two integers
 * a: first integer
 * b: second integer
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - divides one integer by another
 * a: first integer
 * b: second integer
 */
int div(int a, int b)
{
	return ((float)a / b);
}

/**
 * mod - divides one integer by another to find remainder
 * a: first integer
 * b: second integer
 */
int mod(int a, int b)
{

	return (a % b);
}
